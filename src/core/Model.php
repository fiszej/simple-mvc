<?php
declare(strict_types=1);

namespace app\src\core;

use app\src\Request;

abstract class Model 
{
    public function getDataFromRequest(array $data)
    {
        foreach ($data as $key => $value) {
            if (property_exists($this, $key)) {
                $this->{$key} = $value;
            }
        }
    }

    public function loadFromDatabase($data)
    {
        foreach ($data as $key => $value) {
            if (property_exists($this, $key)) {
                $this->{$key} = $value;
            }
        }
    }
}
